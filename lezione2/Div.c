/* Dividere un intero per un altro */

#include <stdio.h>

int main(void)
{
	printf("So fare le divisioni intere: %d : %d = %d\n",17,25,17/25);
	printf("So fare le divisioni intere: %d : %d = %d\n",25,17,25/17);
	printf("So fare le divisioni intere: %d : %d = %d\n",-25,17,(-25)/17);
	printf("So fare le divisioni intere: %d : %d = %d\n",25,-17,25/(-17));
	printf("So fare le divisioni intere: %d : %d = %d\n",-25,-17,(-25)/(-17));
    
	return 0;
}
